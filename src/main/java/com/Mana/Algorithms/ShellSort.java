package com.Mana.Algorithms;

public class ShellSort {
    public static void main(String[] args) {
        System.out.println("------------------Shell Sorting------------------");
        //Variation of Insertion
        //Almost Sorted -> O(n)
        int[] shell={20,35,-15,7,55,1,-22};



        for(int ele:shell){
            System.out.print(ele+",");
        }
    }
}
